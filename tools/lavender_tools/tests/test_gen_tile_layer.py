"""test_gen_tile_layer.py — Unit tests for tile layer registrar code generation.

Tests verify that _generate_registrar_c produces correct C type names
for single-layer and multi-layer configurations, ensuring non-first
layers do NOT get prefixed logical kind or record type names.
"""

import os
import tempfile
from unittest.mock import patch

import pytest

from lavender_tools.gen_tile_layer import (
    _create_tile_kind_h,
    _generate_registrar_c,
    LayerSpec,
)

# Minimal registrar .c template with empty GEN regions.
# _generate_registrar_c uses _set_region(last=True) for TABLES,
# so the SECOND GEN-TABLES pair (inside the function) is the one modified.
_REGISTRAR_TEMPLATE = """\
#include "world/implemented/tile_logic_table_registrar.h"
#include "world/tile_logic_context.h"
#include "defines_weak.h"
#include "types/implemented/world/tile_layer.h"

// GEN-INCLUDE-BEGIN
// GEN-INCLUDE-END

///
/// These comments are autogenerated and
/// modifying them will break automation.
///
// GEN-TABLES-BEGIN
// Tile_Layer__Default:
// GEN-TABLES-END

void register_tile_logic_tables(
        Game *p_game,
        Tile_Logic_Context *p_tile_logic_context) {
    // GEN-TABLES-BEGIN
    // GEN-TABLES-END
    
    // GEN-RECORDS-BEGIN
    // GEN-RECORDS-END
}
"""


@pytest.fixture
def captured_registrar_c():
    """Yield a dict that captures the content written by _generate_registrar_c.

    Patches:
      * _REGISTRAR_C -> temp file containing the template above
      * _write -> writes into the dict under key 'content' instead of disk
      * tool_history -> no-op mock (prevents side-effects from _write)
    """
    with tempfile.NamedTemporaryFile(
        mode="w", suffix=".c", delete=False
    ) as f:
        f.write(_REGISTRAR_TEMPLATE)
        temp_path = f.name

    captured = {}

    def fake_write(path, content):
        captured["path"] = path
        captured["content"] = content

    with patch("lavender_tools.gen_tile_layer._REGISTRAR_C", temp_path), \
         patch("lavender_tools.gen_tile_layer._write", side_effect=fake_write), \
         patch("lavender_tools.gen_tile_layer.tool_history"):
        yield captured

    os.unlink(temp_path)


@pytest.fixture
def captured_kind_h(tmp_path):
    """Capture content written by _create_tile_kind_h."""
    captured = {}

    def fake_write(path, content):
        captured["path"] = path
        captured["content"] = content

    with patch("lavender_tools.gen_tile_layer.PROJECT_ROOT", str(tmp_path)), \
         patch("lavender_tools.gen_tile_layer._write", side_effect=fake_write), \
         patch("lavender_tools.gen_tile_layer.tool_history"):
        yield captured


