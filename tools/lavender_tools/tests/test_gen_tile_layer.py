def test_multi_layer__gen_includes_all_layers(captured_registrar_c):
    """GEN-INCLUDE must contain tile_logic_context.h and all layer tables."""
    specs = [
        LayerSpec("Ground", total_bits=8, logic_bits=4, animation_bits=2),
        LayerSpec("Structure", total_bits=8, logic_bits=4, animation_bits=2),
        LayerSpec("Cover", total_bits=8, logic_bits=4, animation_bits=2),
    ]
    _generate_registrar_c(specs, ["Ground", "Structure", "Cover"])
    content = captured_registrar_c["content"]

    include_start = content.find("// GEN-INCLUDE-BEGIN")
    include_end = content.find("// GEN-INCLUDE-END", include_start)
    include_region = content[include_start:include_end]

    assert '#include "world/tile_logic_context.h"' in include_region
    assert '#include "world/implemented/tile_logic_table__ground.h"' in include_region
    assert '#include "world/implemented/tile_logic_table__structure.h"' in include_region
    assert '#include "world/implemented/tile_logic_table__cover.h"' in include_region


def test_multi_layer__gen_records_all_layers(captured_registrar_c):
    """GEN-RECORDS must contain registration calls for every layer."""
    specs = [
        LayerSpec("Ground", total_bits=8, logic_bits=4, animation_bits=2),
        LayerSpec("Structure", total_bits=8, logic_bits=4, animation_bits=2),
        LayerSpec("Cover", total_bits=8, logic_bits=4, animation_bits=2),
    ]
    _generate_registrar_c(specs, ["Ground", "Structure", "Cover"])
    content = captured_registrar_c["content"]

    records_start = content.find("// GEN-RECORDS-BEGIN")
    records_end = content.find("// GEN-RECORDS-END", records_start)
    records_region = content[records_start:records_end]

    assert "register_tile_logic_table__ground(" in records_region
    assert "register_tile_logic_table__structure(" in records_region
    assert "register_tile_logic_table__cover(" in records_region

    # Each call must reference the correct Tile_Layer enum value.
    assert "Tile_Layer__Ground" in records_region
    assert "Tile_Layer__Structure" in records_region
    assert "Tile_Layer__Cover" in records_region


