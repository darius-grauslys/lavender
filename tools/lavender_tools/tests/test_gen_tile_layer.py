def test_single_layer__uses_tile_kind_logical(captured_registrar_c):
    """Ground (first layer) should use Tile_Kind__Logical and Tile_Logic_Record."""
    spec = LayerSpec("Ground", total_bits=8, logic_bits=4, animation_bits=2)
    _generate_registrar_c([spec], ["Ground"])
    content = captured_registrar_c["content"]

    assert "Tile_Kind__Logical" in content
    assert "sizeof(Tile_Logic_Record)" not in content


